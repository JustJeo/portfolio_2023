            <p>At first, my boss wanted to skip creating a wireframe, stating that he usually doesn't make one, but I took the initiative to create one anyways. I found it easier to work if I had a rough outline of where the content and sections were going to go. Using a web application called "Excalidraw," I was able to mock up a website in an hour. The General Manager and the Senior Web Developer were very impressed with my wireframes that they decided, from then on, that a wireframe would have to be included for the clients to approve. It was a great way of starting the process without going into the full build.</p>

            <p>The website consisted of 6 main pages and was overall a hefty website in the media department. A majority of these pages had multiple informational tabs, and each tab contained image carousels with at least 3 images. The landing page [pictured here] instantly loaded a 20-second video, which was provided by the client, that would loop over and over again. It's probably my biggest project to date.</p>
