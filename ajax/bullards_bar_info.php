            <p>The website creation for Bullard's Bar was my first-ever client project. It was for a boating company that would allow visitors to rent out a variety of water vehicles ranging from giant Houseboats for the week, to jet skis by the hour. The owner of the company had reached out to Tech 2U from a recommendation and was hoping that we could help modernize their website for them. Their original website was a bit outdated with pixelated photos and info that needed to be revised. Thankfully, the client was very cooperative and communicative. He was willing to take newer photos and provide the info that needed to be changed.</p>

