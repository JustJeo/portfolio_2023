						<h5 class="ac-title">Does my computer need a tune up?</h5>
						<div class="ac-content">
							<p>If you haven't had a tune-up on your PC in the last 6 months, then the answer is likely "yes". Very similar to a car, you want to make sure that your computer is in good condition, both inside and out. Although you may not be able to physically see any software maintenance needed, it's best to check if one is needed. Typically, a tune up should be done once every 3 months. If you want, a tune up every single month is even better for your PC.</p>
						</div>
					</div>
					<div class="ac-item">
						<h5 class="ac-title">When is it time to buy a new computer?</h5>
						<div class="ac-content">
							<p>Your computer's life span will depend on several different factors. How old is the hardware and what version is the software? Is your PC used every day for hours on end or every week for light errands and tasks? Your stereotypical PC gamer may need to change out and update their setup after 2 years. In comparison, a lightly used PC may last a few years longer than that. On average, office computers are replaced every three to five years. In many cases, these machines get replaced because they are no longer as efficient as they need to be. With the newest versions of technology constantly coming out, it's easy for devices to get outdated.</p>
						</div>
					</div>
					<div class="ac-item">
						<h5 class="ac-title">What's the difference between shutting down your PC and putting it in sleep mode?</h5>
						<div class="ac-content">
							<p>Study shows that it is okay to leave your computer on all day if you plan on using it multiple times a day for many hours at a time. Placing your computer in sleep mode in-between those uses helps your PC conserve energy while still not fully shutting down. However, if you only use your computer occasionally and for a short amount of time (say once a day for only 2 hours) it's best to turn it off after using it. Shutting down your PC completely and then turning it back on can actually damage your machine if done repeatedly in a short period of time, so use your best judgment on whether you should choose to fully shut down your computer or simply set it in sleep mode.</p>
						</div>
					</div>
					<div class="ac-item">
						<h5 class="ac-title">Can I share one printer?</h5>
						<div class="ac-content">
							<p>With Windows 10, it is possible to share your printer with many PCs on your network. Ensure that all devices that plan on printing have the most updated version of the printing software. This will depend on the make and model of your printer. Next, connect all of the devices to the same network as the printer. Older printers may require the PC to be connected with a cable, but newer printers generally allow wireless printing. A prompt will typically pop up on the device's screen to verify which printer you wish to print with, and after that your printer should start printing.</p>
