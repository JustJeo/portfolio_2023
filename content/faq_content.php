						<h5 class="ac-title">How do I protect my data?</h5>
						<div class="ac-content">
							<p>The number one thing to do is to avoid downloading files from any website that you don't trust completely. This includes clicking on suspicious links, advertisements, or filling out pop-up forms. Any information that you enter on social media websites, shopping accounts, or anything on any website could potentially be taken in the event of a data breach. What goes on the internet can be grabbed by the internet.</p>
							
							<p>Two-factor authentication is an additional layer of security that provides protection in the event that a hacker guesses or cracks your password. Two-factor authentication requires a second verification step, such as the answer to a secret question or a personal identification number (PIN) that can be sent to your phone or email. You should opt for two-factor authentication when given an option by a website.</p>
						</div>
					</div>
					<div class="ac-item">
						<h5 class="ac-title">How do I stay safe on the internet?</h5>
						<div class="ac-content">
							<p>Relying on a public Wi-Fi connection means your data is more likely to be seen by outsiders even if you are on your own personal device. Public hotspots or "Free Wi-Fi" connections do not have as many layers of protection so that other people can easily access the internet too. The best thing to do is to avoid sending sensitive information over any public Wi-Fi network.<p>

							<p>You should also refrain from sending personal information when using a device that isn't yours. For example, many people like to access their social media accounts on computers at the school library, but many users forget to sign out when they're finished. This means that all of their info is up for grabs by the next person that uses that computer. A great practice is to log out of any website you have logged into and delete the browser's history for extra measure.</p>
						</div>
					</div>
					<div class="ac-item">
						<h5 class="ac-title">What are some safety tips for using social media?</h5>
						<div class="ac-content">
							<p>If you receive an email from a source or individual you don't recognize, don't open it, and <b>definitely avoid</b> clicking any links or file attachments. Too many people have fallen victim to scams online by believing the empty promises of accquiring fame or fortune. Scammers will do anything to make their links seem genuine or worth it. Some will even pose as long lost friends or family in order for you to trust them. The bottom line is, if you weren't expecting it, then don't open it.</p>
