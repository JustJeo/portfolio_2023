						<h5 class="ac-title">Does my computer have a virus?</h5>
						<div class="ac-content">
							<p>Just because your PC is running fine doesn't mean it doesn't have any malware. The word "Malware" comes from the combined words of "Malicious" and "Software" and is the catch-all term for any device or data threatening software. Malware can have many forms, such as viruses, ransomeware, bots, etc.</p>
							
							<p>The viruses in the beginning of the internet era were often pranks and jokes that ran wild and used a lot of system resources to slow down your computer. Malware now-a-days are more likely to lurk silently and covertly in the background, trying to evade detection so it can capture your credit card numbers, login information, and other personal data. In other words, modern-day malware is often created by criminals just to make money, and well-crafted malware won't cause any noticeable PC problems at all. It's a great practice to scan your computer at least once a week with an antivirus program.</p>
						</div>
					</div>
					<div class="ac-item">
						<h5 class="ac-title">What is a computer virus?</h5>
						<div class="ac-content">
							<p>A computer virus is one of the many different types of malware. Specifically, it is a malicious piece of computer code designed to spread from one device to another device. These threats are usually designed to copy itself over and over again to damage a device or to steal data from it. It is meant to infect other devices by latching onto downloadable files, website links, etc. To avoid getting a virus on a computer, do not click or download anything from a source you don't trust.</p>
						</div>
					</div>
					<div class="ac-item">
						<h5 class="ac-title">Can I have more than one antivirus program on my computer?</h5>
						<div class="ac-content">
							<p>The short answer is yes you can, but you definitely shouldn't run them at the same time. Contrary to popular belief, you will not be getting extra protection. If you run more than one antivirus program at the same time, they will compete <b><em>against</em></b> each other. Antivirus programs do not see each other as allies and will instead dub the other as the enemy. Instead of protection, you will end up with two weak programs that are fighting one another and potentially missing the real threat of any malware.</p>
